export const getAirlineName = (callsign) => {
    if (!callsign) return "Unknown Airline";
    // ICAO callsigns usually start with 3 letters
    const prefix = callsign.substring(0, 3).toUpperCase();
    return airlines[prefix] || prefix; // Return name or just prefix if unknown
};
